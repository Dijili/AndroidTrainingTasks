package com.example.androidtraining_tasks.constraintlayout;

public class HouHeaderCardData {

    public int image;
    public String name;

    public HouHeaderCardData(int image, String name) {
        this.image = image;
        this.name = name;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
